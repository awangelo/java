public class StringSplit {
	public static void main(String[] args) {
		String msg = "Sou uma string";
		String[] msgSplitArray = msg.split(" ");

		
		System.out.println(msg);
		System.out.println(msgSplitArray[0]);
		System.out.println(msgSplitArray[1]);
		System.out.println(msgSplitArray[2]);
	}
}
